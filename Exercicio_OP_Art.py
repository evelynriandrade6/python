# Definir dois números para calcular
numero1 = float( input("Digite um numero: ") )
numero2 = float( input("Digite outro numero: ") )

# Se o usuário quiser somar
Adição = numero1 + numero2

# Se caso o usuário quiser subtrair
Subtração = numero1 - numero2

# Se caso o usuário quiser multiplicar
Multiplicação = numero1 * numero2

# Se caso o usuário dividir
Divisão = numero1 / numero2

# Se caso o usuário quiser a divisão inteira
DivisãoIn = numero1 // numero2

# Se caso o usuário quiser o resto da divisão
RestoDiv = numero1 % numero2

# Se caso o usuário quiser a potência
Potência = numero1 ** numero2

print("Adição:", numero1, '+', numero2, '=', Adição)

print("Subtração:", numero1, '-', numero2, '=', Subtração)

print("Multiplicação:", numero1, '*', numero2, '=', Multiplicação)

print("Divisão:", numero1, '/', numero2, '=', Divisão)

print("DivsãoIn:", numero1, "//", numero2, '=', DivisãoIn)

print("RestoDiv:", numero1, '%', numero2, '=', RestoDiv)

print("Potência:", numero1, '**', numero2, '=', Potência)

